void soundInit(){
  NR52_REG = 0x80;   //sound on
  NR50_REG = 0x77;   //max volume left/right
  NR51_REG = 0xFF;   //route all channels
  NR21_REG = 0x80;   //channel 2 duty 50%, length 0 (full)
  NR22_REG = 0x73;   //envelope start vol 7 (about 50%), decay, step=3

  // noise channel defaults
  NR41_REG = 0x00;   //length
  NR42_REG = 0x00;   //envelope off until triggered
  NR43_REG = 0x00;   //will be configured per hit

  // preload wave channel for channel 3 SFX
  NR30_REG = 0x00;   //disable to write wave RAM
  for (i = 0; i < 16; i++)
    AUD3WAVE[i] = channel3Wave[i];
  NR30_REG = 0x80;   //enable channel 3
  NR31_REG = 0x00;   //full length until we trigger
  NR32_REG = 0x00;   //mute until use

  activeWaveSteps = 0;
  activeWaveStepCount = 0;
  waveSfxActive = 0;
  waveStepPos = 0;
  waveTimer = 0;

  // lead channel setup
  NR10_REG = 0x00;    // sweep off (adjust if you want sweep)
  NR11_REG = 0x80;    // 50% duty, length 0 (full)
  NR12_REG = 0x73;    // envelope: vol=7 (about 50%), decay, step=3
  // Route is already full in NR51=0xFF; if not, ensure bit0/4 are on for ch1

  stopMusic(); //start silent until gameplay begins
}

void waveSfxUpdate(){
  if (!waveSfxActive || activeWaveSteps==0)
    return;

  if (waveTimer == 0){
    if (waveStepPos >= activeWaveStepCount){
      NR32_REG = 0x00; //mute channel 3 when done
      waveSfxActive = 0;
      return;
    }

    const SfxStep* step = &activeWaveSteps[waveStepPos++];
    NR30_REG = 0x80;  // ensure channel enabled
    NR32_REG = step->volume;
    NR31_REG = 0x00;  // full length, we handle timing
    NR33_REG = (unsigned char)(step->note & 0xFF);
    NR34_REG = (unsigned char)(((step->note >> 8) & 0x07) | 0x80); // trigger

    waveTimer = step->duration;
  }

  if (waveTimer > 0)
    waveTimer--;

}

void musicUpdate(){
  if (!musicPlaying){
    return;
  }


  //loop the song using absolute timestamps
  if (musicFrame >= activeTotalFrames){
    musicFrame = activeLoopFrame;
    musicPos1 = 0;
    musicPos2 = 0;
    musicPos3 = 0;
    musicTimer1 = 0;
    musicTimer2 = 0;
    musicTimer3 = 0;
    NR42_REG = 0x00;
  }

  //channel 1 (lead/harmony)
  while (musicPos1 < activeLength1 && musicFrame == activeChannel1Track[musicPos1].timestamp){
    const MusicNote* note1 = &activeChannel1Track[musicPos1];
    musicPos1++;
    musicTimer1 = note1->duration;

    if (note1->note==NOTE_REST){
      NR12_REG = 0x00; //rest
    }
    else{
      NR12_REG = 0x73; //refresh envelope at ~50% volume
      NR13_REG = (unsigned char)(note1->note & 0xFF);
      NR14_REG = (unsigned char)(((note1->note>>8)&0x07) | 0x80); //trigger
    }
  }

  //channel 2 (bass/backing)
  while (musicPos2 < activeLength2 && musicFrame == activeChannel2Track[musicPos2].timestamp){
    const MusicNote* note2 = &activeChannel2Track[musicPos2];
    musicPos2++;
    musicTimer2 = note2->duration;

    if (note2->note==NOTE_REST){
      NR22_REG = 0x00; //rest
    }
    else{
      NR22_REG = 0x73; //refresh envelope at ~50% volume
      NR23_REG = (unsigned char)(note2->note & 0xFF);
      NR24_REG = (unsigned char)(((note2->note>>8)&0x07) | 0x80); //trigger
    }
  }

  //channel 4 (noise percussion)
  while (musicPos3 < activeLength3 && musicFrame == activeChannel3Track[musicPos3].timestamp){
    const MusicNote* hit = &activeChannel3Track[musicPos3];
    musicPos3++;
    musicTimer3 = hit->duration/4;

    if (disableNoise==0)
    {
      if (hit->note==NOTE_REST){
        NR42_REG = 0x00; //rest
      }
      else{
        NR41_REG = 0x00; //full length, we gate manually via timer
        NR42_REG = 0x52; //volume envelope for percussive hit
        NR43_REG = 0x15; //medium noise tone
        NR44_REG = 0x80; //trigger noise
      }
    }
    
  }

  //count down active notes so they end cleanly between timestamps
  if (musicTimer1>0){
    musicTimer1--;
    if (musicTimer1==0)
      NR12_REG = 0x00; //silence until the next timestamped note
  }

  if (musicTimer2>0){
    musicTimer2--;
    if (musicTimer2==0)
      NR22_REG = 0x00;
  }

  if (musicTimer3>0){
    musicTimer3--;
    if (disableNoise==0)
    {
      if (musicTimer3==0)
        NR42_REG = 0x00;
    }
  }

  //advance global music clock (60 ticks per second)
  musicFrame++;
}